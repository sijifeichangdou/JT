package com.jt.manage.pojo;

import java.util.List;

public class ItemCatData {
	private String url;
	private String name;
	private List<?> ItemCatData;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<?> getItemCatData() {
		return ItemCatData;
	}
	public void setItemCatData(List<?> itemCatData) {
		ItemCatData = itemCatData;
	}
	
}
